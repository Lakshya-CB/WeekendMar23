package Lec_07;

public class char_demo {
	public static void main(String[] args) {
		char ch = 'a';
		System.out.println(ch);
		System.out.println(ch+0);
		
		ch='b';
		System.out.println((int)ch);
	
		ch='z';
		System.out.println((int)ch);
		
		
		ch=' ';
		System.out.println((int)ch);
		
		System.out.println("===========");
		
		ch = '(';
		System.out.println((int)ch);
		
		System.out.println('d'-'a');
	}
	
}
