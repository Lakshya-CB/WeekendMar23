package Lec_06;

public class data_type_demo {
	public static void main(String[] args) {
		byte b = (byte)250;
		System.out.println(b);

		short s = 32000;
		System.out.println(s);
		
		int i = 2000000000;
		System.out.println(i);
		
		long l = (byte)2L;
		System.out.println(l);
	
//		#####################
		float f = (float)10.1;
		double d = 10.1;
	}
		
}
